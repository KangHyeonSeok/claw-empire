#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const deployDockerDir = path.join(rootDir, "deploy", "docker");
const configPath = path.join(deployDockerDir, "fleet.config.json");
const envTemplatePath = path.join(deployDockerDir, "company.env.template");
const runtimeDir = path.join(deployDockerDir, "runtime");
const outputPath = path.join(deployDockerDir, "compose.multi-company.yml");

const SHARED_AUTH_VOLUMES = [
  { volume: "shared_claude_auth", target: "/home/app/.claude" },
  { volume: "shared_codex_auth", target: "/home/app/.codex" },
  { volume: "shared_gemini_auth", target: "/home/app/.gemini" },
  { volume: "shared_opencode_auth", target: "/home/app/.local/share/opencode" },
];

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function assertString(value, label) {
  if (typeof value !== "string" || value.trim().length === 0) {
    throw new Error(`${label} must be a non-empty string`);
  }
  return value.trim();
}

function assertPort(value, label) {
  if (!Number.isInteger(value) || value <= 0) {
    throw new Error(`${label} must be a positive integer`);
  }
  return value;
}

function volumeKey(slug, suffix) {
  return `${slug.replace(/-/g, "_")}_${suffix}`;
}

function companyEnvPath(slug) {
  return `./runtime/${slug}.env`;
}

function isSharedCliAuthEnabled(config) {
  return config.sharedCliAuth !== false;
}

function buildCompose(config) {
  const stackName = assertString(config.name, "name");
  const image = assertString(config.image, "image");
  const companies = Array.isArray(config.companies) ? config.companies : [];

  if (companies.length === 0) {
    throw new Error("companies must contain at least one entry");
  }

  const serviceLines = [];
  const volumeLines = [];
  const seenSlugs = new Set();
  const seenPorts = new Set();
  const sharedCliAuthEnabled = isSharedCliAuthEnabled(config);

  for (const company of companies) {
    const slug = assertString(company.slug, "company.slug");
    const port = assertPort(company.hostPort, `company.hostPort (${slug})`);

    if (seenSlugs.has(slug)) throw new Error(`duplicate company slug: ${slug}`);
    if (seenPorts.has(port)) throw new Error(`duplicate hostPort: ${port}`);
    seenSlugs.add(slug);
    seenPorts.add(port);

    const dataVolume = volumeKey(slug, "data");
    const logsVolume = volumeKey(slug, "logs");

    serviceLines.push(`  ${slug}:`);
    serviceLines.push(`    <<: *claw-empire-service`);
    serviceLines.push(`    container_name: claw-empire-${slug}`);
    serviceLines.push(`    env_file:`);
    serviceLines.push(`      - ${companyEnvPath(slug)}`);
    serviceLines.push(`    ports:`);
    serviceLines.push(`      - \"${port}:8790\"`);
    serviceLines.push(`    volumes:`);
    serviceLines.push(`      - ${dataVolume}:/var/lib/claw-empire`);
    serviceLines.push(`      - ${logsVolume}:/var/log/claw-empire`);
    if (sharedCliAuthEnabled) {
      for (const sharedVolume of SHARED_AUTH_VOLUMES) {
        serviceLines.push(`      - ${sharedVolume.volume}:${sharedVolume.target}`);
      }
    }
    serviceLines.push("");

    volumeLines.push(`  ${dataVolume}:`);
    volumeLines.push(`  ${logsVolume}:`);
  }

  return [
    "# This file is generated by scripts/generate-docker-fleet.mjs.",
    "# Edit deploy/docker/fleet.config.json instead of editing this file directly.",
    `name: ${stackName}`,
    "",
    "x-claw-empire-service: &claw-empire-service",
    `  image: ${image}`,
    "  build:",
    "    context: ../..",
    "    dockerfile: Dockerfile",
    "  restart: unless-stopped",
    "  stop_grace_period: 30s",
    "  environment:",
    "    NODE_ENV: production",
    "    HOST: 0.0.0.0",
    "    PORT: 8790",
    "  healthcheck:",
    "    test:",
    "      [",
    "        \"CMD\",",
    "        \"node\",",
    "        \"-e\",",
    "        \"fetch('http://127.0.0.1:8790/healthz').then((res)=>{if(!res.ok) process.exit(1);}).catch(()=>process.exit(1))\",",
    "      ]",
    "    interval: 30s",
    "    timeout: 5s",
    "    start_period: 40s",
    "    retries: 5",
    "",
    "services:",
    ...serviceLines,
    "volumes:",
    ...volumeLines,
    ...(sharedCliAuthEnabled ? SHARED_AUTH_VOLUMES.map((entry) => `  ${entry.volume}:`) : []),
    "",
  ].join("\n");
}

function buildEnvFile(template, company) {
  const slug = assertString(company.slug, "company.slug");
  const port = assertPort(company.hostPort, `company.hostPort (${slug})`);
  const publicHost = typeof company.publicHost === "string" && company.publicHost.trim() ? company.publicHost.trim() : "localhost";
  const oauthBaseUrl = typeof company.oauthBaseUrl === "string" && company.oauthBaseUrl.trim()
    ? company.oauthBaseUrl.trim()
    : `http://${publicHost}:${port}`;

  return template
    .replaceAll("__COMPANY_SLUG__", slug.toUpperCase().replace(/-/g, "_"))
    .replaceAll("__HOST_PORT__", String(port))
    .replaceAll("__OAUTH_BASE_URL__", oauthBaseUrl);
}

function ensureRuntimeFiles(config) {
  fs.mkdirSync(runtimeDir, { recursive: true });
  const template = fs.readFileSync(envTemplatePath, "utf8");

  for (const company of config.companies) {
    const slug = assertString(company.slug, "company.slug");
    const targetPath = path.join(runtimeDir, `${slug}.env`);
    if (fs.existsSync(targetPath)) continue;
    fs.writeFileSync(targetPath, buildEnvFile(template, company), "utf8");
    console.log(`[docker-fleet] created ${path.relative(rootDir, targetPath)}`);
  }
}

function main() {
  const config = readJson(configPath);
  const compose = buildCompose(config);
  ensureRuntimeFiles(config);
  fs.writeFileSync(outputPath, compose, "utf8");
  console.log(`[docker-fleet] wrote ${path.relative(rootDir, outputPath)}`);
}

main();
